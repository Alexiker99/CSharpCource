void PrintImage(int[,] image) // Метод, который выводит картинку в терминал
{
 for (int i = 0; i < image.GetLength(0); i++) // цикл для 0
 {
 for (int j = 0; j < image.GetLength(1); j++) // цикл для 1
 {
 if(image[i,j] == 0) Console.Write($" "); 
 else Console.Write($"+");
 }
 Console.WriteLine();
 }
}
void FillImage(int row, int col) // Метод, который будет закрашивать картинку. 
{
 if (pic[row, col] == 0) 
 {
 pic[row, col] = 1;
 FillImage(row - 1, col);
 FillImage(row, col - 1);
 FillImage(row + 1, col);
 FillImage(row, col + 1);
 }
}

